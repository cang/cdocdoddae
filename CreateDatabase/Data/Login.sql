USE [#DBNAME]

CREATE LOGIN [DDADBUser] WITH PASSWORD=N'#PASSWORD', DEFAULT_DATABASE=[#DBNAME], DEFAULT_LANGUAGE=[us_english], CHECK_EXPIRATION=OFF, CHECK_POLICY=OFF
GO

ALTER LOGIN [DDADBUser] ENABLE
GO

	IF NOT EXISTS (SELECT * FROM sys.database_principals WHERE name = N'DDADBUser')
	CREATE USER [DDADBUser] FOR LOGIN [DDADBUser] WITH DEFAULT_SCHEMA=[dbo]
	GO

	sp_addrolemember 'db_owner', 'DDADBUser'
	GO